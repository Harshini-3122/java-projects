public class operaters {
    public static void main(String[] args){
        int x = 16;
        int y = 8;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);

        int a = 31;
        a--;
        System.out.println(a);
    }
}
